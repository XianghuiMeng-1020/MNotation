type I18nValue = {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: (key: string, vars?: Record<string, string | number>) => string;
};

const I18nContext = createContext<I18nValue | null>(null);

function defaultLocale(): Locale {
  const saved = localStorage.getItem("mnotation_locale");
  if (saved === "zh-Hans" || saved === "zh-Hant" || saved === "en") return saved;
  const nav = navigator.language.toLowerCase();
  if (nav.startsWith("zh-tw") || nav.startsWith("zh-hk")) return "zh-Hant";
  if (nav.startsWith("zh")) return "zh-Hans";
  return "en";
}

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [locale, setLocaleRaw] = useState<Locale>(() => defaultLocale());
  const setLocale = (next: Locale) => {
    setLocaleRaw(next);
    localStorage.setItem("mnotation_locale", next);
  };
  const value = useMemo<I18nValue>(() => ({
    locale,
    setLocale,
    t: (key, vars) => {
      const raw = messages[locale][key] ?? messages.en[key] ?? key;
      if (!vars) return raw;
      return Object.entries(vars).reduce((acc, [k, v]) => acc.replaceAll(`{${k}}`, String(v)), raw);
    }
  }), [locale]);
  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const value = useContext(I18nContext);
  if (!value) throw new Error("useI18n must be used within I18nProvider");
  return value;
}
